//interface concept
//in interface by default all method are abstract, public,and final
//interface can run program without implementing it
//but generally interface is 100%abstract (100% consist incomplete method)
//so to complete that incomplete method we use implement class
// one more thing is interface don't have constructor
package otherpackage;

public interface Abc {
	
	        int a=3;
			int b=6;
	        int c=a+b;
	
	
	public static void main(String[] args) {
		System.out.println(c);
	}

	

}
